//multiplication of a number using pointer
#include <stdio.h>

int main() {
    int num, result;
    int *ptr = &num;

    printf("Enter a number: ");
    scanf("%d", ptr);

    result = *ptr * 5; // Multiply the number by 5

    printf("Result: %d\n", result);

    return 0;
}

